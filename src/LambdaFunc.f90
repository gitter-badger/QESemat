FUNCTION LambdaFunc(a,b,c)
!----------------------------------------------------------------------!
!a^2+b^2+c^2-2*(a*b+b*c+a*c)
!----------------------------------------------------------------------!
!edited by                                                    O.Petrova!
!**********************************************************************!

implicit none

real:: &
    LambdaFunc

real &
    a,b,c

    LambdaFunc=a*a+b*b+c*c-2*(a*b+b*c+a*c)
    return
!----------------------------------------------------------------------!
endFUNCTION LambdaFunc
